  def __init__(self):
    print("dataset class object created")

    
  def load_data(self, dataset):
    print("loading Dataset ",dataset)
    
    if (dataset=="cifar10"):
      return cifar10.load_data()
      
    elif (dataset=='mnist'):
      return mnist.load_data()
        
    else:
      print("Error could not load data ")

